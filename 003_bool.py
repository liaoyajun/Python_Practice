right = True
wrong = False
print('\nright的值是True，wrong的值是False。')

not_right = not right
print('\nnot right 的值: ', not_right)

not_wrong = not wrong
print('\nnot wrong 的值: ', not_wrong)

right_and_wrong = right and wrong
print('\nright and wrong 的值: ', right_and_wrong)

right_or_wrong = right or wrong
print('\nright or wrong 的值: ', right_or_wrong)


